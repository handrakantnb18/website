package com.zeroToHero.core;

public class Employee {

	private Integer id;
	
	private String name;
	
	private String city;
	
	private String dept;
	
	private Double mobileNo;

	@Override
	public String toString() {
		return "Employee [id=" + id + ", name=" + name + ", city=" + city + ", dept=" + dept + ", mobileNo=" + mobileNo
				+ "]";
	}
	
	
}
