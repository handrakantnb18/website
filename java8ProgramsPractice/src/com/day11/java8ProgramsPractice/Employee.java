package com.day11.java8ProgramsPractice;

public class Employee {

	private Integer id;
	
	private String name;
	
	private String address;
	
	private String salary;
	
	private String dept;

	@Override
	public String toString() {
		return "Employee [id=" + id + ", name=" + name + ", address=" + address + ", salary=" + salary + ", dept="
				+ dept + "]";
	}
	
	
}
