public class Student {

	private Integer id;
	
	private String name;
	
	private String collage;
	
	private String email;
	
	private String mobileNo;

	@Override
	public String toString() {
		return "Student [id=" + id + ", name=" + name + ", collage=" + collage + ", email=" + email + ", mobileNo="
				+ mobileNo + "]";
	}
	
	
}
