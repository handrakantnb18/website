public class User {

	private Integer id;
	
	private String name;
	
	private String address;
	
	private String email;
	
	private String mobileNo;

	@Override
	public String toString() {
		return "User [id=" + id + ", name=" + name + ", address=" + address + ", email=" + email + ", mobileNo="
				+ mobileNo + "]";
	}
	
	
}
