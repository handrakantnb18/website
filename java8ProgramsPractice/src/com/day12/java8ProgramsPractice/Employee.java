package com.day12.java8ProgramsPractice;

public class Employee {

	private Integer id;
	
	private String name;
	
	private String dept;
	
	private String salary;
	
	private String city;

	@Override
	public String toString() {
		return "Employee [id=" + id + ", name=" + name + ", dept=" + dept + ", salary=" + salary + ", city=" + city
				+ "]";
	}
	
	
}
